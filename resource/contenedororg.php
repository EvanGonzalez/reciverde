                        <p>El contenedor marrón es el encargado de recoger los residuos orgánicos, es decir, todos los residuos de origen animal o vegetal que se generan en tu hogar, como restos de comida, cáscaras de fruta, servilletas usadas o flores marchitas. Estos residuos se transportan a una planta de compostaje, donde se transforman en compost, un abono natural y ecológico que se puede usar para fertilizar el suelo y las plantas.</p>
                        <p>El compostaje es un proceso natural que imita el ciclo de la vida, donde la materia orgánica se descompone con la ayuda de microorganismos y oxígeno. El resultado es un producto rico en nutrientes y humus, que mejora las propiedades físicas, químicas y biológicas del suelo. Además, el compostaje evita que los residuos orgánicos acaben en el vertedero, donde generan gases de efecto invernadero y contaminan el medio ambiente.</p>

                        <blockquote class="pullquote"> "El contenedor marrón es el quinto contenedor de reciclaje que se suma a los ya conocidos: azul (papel y cartón), amarillo (envases), verde (vidrio) y gris (resto). Su implantación responde a los objetivos de la Unión Europea de reducir la generación de residuos y aumentar el reciclaje y la reutilización. Según Ecoembes, más del 40% de nuestras bolsas de basura incluyen bioresiduos que son cien por cien aprovechables."</blockquote>

                        <p>
                            Para usar correctamente el contenedor marrón, debes depositar en él solo los residuos orgánicos reciclables, como los que te hemos mencionado antes. No debes tirar en él otros residuos como aceite, cenizas, pañales, colillas, chicles o plásticos. También es importante usar bolsas biodegradables o de papel para envolver los residuos, ya que facilitan el proceso de compostaje y mantienen la higiene del contenedor.
                        </p>

